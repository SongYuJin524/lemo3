	/*
	 * 获取当前细胞周围细胞情况
	 */
	public int get_neighbor_count(int row ,int col) {
		int count = 0, r = 0, c = 0;  
		  //行列双循环计数
        for (r = row - 1; r <= row + 1; r++) {  
            for (c = col - 1; c <= col + 1; c++) {  
            	//遇到边界忽略继续
                if (r < 0 || r >= tempMap.length || c < 0  || c >= tempMap[0].length) {  
                    continue;  
                }  
                if (tempMap[r][c] == ALIVE) {  
                    count++;  
                }  
            }  
        } 
        //前面扫描九宫格包含当前细胞   判断当前细胞状态 用以排除
        if (tempMap[row][col] == ALIVE) {  
            count--;  
        }  
        return count;  
	}
